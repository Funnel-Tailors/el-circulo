export const AGREEMENT_VERSIONS: Record<string, string> = { v1: V1, v2: V2 };

export const AGREEMENT_TEXT = AGREEMENT_VERSIONS[AGREEMENT_VERSION];

export function getAgreementText(version?: string | null): string {
  return (version && AGREEMENT_VERSIONS[version]) || AGREEMENT_TEXT;
}

// SHA-256 hex del texto exacto mostrado (Web Crypto, disponible en el navegador).
export async function getAgreementHash(text: string = AGREEMENT_TEXT): Promise<string> {
  const data = new TextEncoder().encode(text);
  const digest = await crypto.subtle.digest("SHA-256", data);
  return Array.from(new Uint8Array(digest))
    .map((b) => b.toString(16).padStart(2, "0"))
    .join("");
}
